void _TreateMessages( BulanParser*& parser, DRMessageManager& dmm, long message_page )
{
	const std::vector<HKLineMessage>& eTbl = parser->GetErrorTable();
	const std::vector<HKLineMessage>& wTbl = parser->GetWarningTable();
	long i;
	for( i = 0; i < (long) eTbl.size(); i++ )
	{
		if( eTbl[ i ].line )
			dmm.AddLine( message_page, eTbl[ i ].text, eTbl[ i ].line );
		else
			dmm.AddLine( message_page, eTbl[ i ].text );
	}
	for( i = 0; i < (long) wTbl.size(); i++ )
	{
		if( wTbl[ i ].line )
			dmm.AddLine( message_page, wTbl[ i ].text, wTbl[ i ].line );
		else
			dmm.AddLine( message_page, wTbl[ i ].text );
	}
}

int _TryCompileBulan( const QString& text, PiCodeASTStorage*& bms, BulanParser*& parser, DRMessageManager& /*dmm*/, long /*message_page*/ )
{
	int ret = 0;
	Source srcV(text);
	parser = new BulanParser( srcV, ::createActionT( (LRTDATA*)bulan_lrt_table ), &bms );
	try
	{
		parser->Make();
		if( parser->IsError() ) ret = -1;
	}
	catch( QString errMsg )
	{
		parser->SetError( errMsg );
		ret = -1;
	}
	if( ret != 0 && bms )
	{
		delete bms;
		bms = 0;
	}
	return ret;
}

int LS_CompileBulanExpr( const QString& _txt, PiCodeASTStorage*& bms, DRMessageManager& dmm, long message_page )
{
	int ret;
	BulanParser *parser = 0;
	ret = ::_TryCompileBulan( _txt, bms, parser, dmm, message_page );
	if( parser )
	{
		::_TreateMessages( parser, dmm, message_page );
		delete parser;
	}
	return ret;
}

